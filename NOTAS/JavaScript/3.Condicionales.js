/*
--Condicionales:
Los condicionales son una estructura de control que permite tomar
decisiones en función de una condición. En JavaScript, se 
utilizan principalmente los condicionales if, else if y else.
*/
let edad = 20;
if (edad >= 18) {
  console.log("Eres mayor de edad");
} else {
  console.log("Eres menor de edad");
}


/*
--Operadores ternarios:
El operador ternario (?) se utiliza para simplificar las estructuras 
de control que toman decisiones basadas en una condición.
*/
let a = 5;
let b = 2;
let resultado = (a > b) ? "a es mayor que b" : "a es menor que b";
console.log(resultado); // muestra "a es mayor que b"
