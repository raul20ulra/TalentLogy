/*
--2--Operadores: En JavaScript existen varios operadores que se pueden
 utilizar para realizar operaciones aritméticas, lógicas y de comparación.
  A continuación, te presento algunos ejemplos de los operadores más comunes:

--2.1--Operadores aritméticos:
*/
{
    //Suma +: permite sumar dos valores.
    let a = 5;
    let b = 3;
    let resultado = a + b; // resultado = 8
    }
    {
    //Resta -: permite restar dos valores.
    let a = 5;
    let b = 3;
    let resultado = a - b; // resultado = 2
    }
    {
    //Multiplicación *: permite multiplicar dos valores.
    let a = 5;
    let b = 3;
    let resultado = a * b; // resultado = 15
    }
    {
    //División /: permite dividir dos valores.
    let a = 6;
    let b = 3;
    let resultado = a / b; // resultado = 2
    }
    {
    //Módulo %: permite obtener el resto de la división entre dos valores.
    let a = 7;
    let b = 3;
    let resultado = a % b; // resultado = 1
    }
    /*
    --2.2--Operadores de comparacion:
    */
    {
    //Igualdad ==: permite comparar si dos valores son iguales.
    let a = 5;
    let b = 5;
    let resultado = a == b; // resultado = true
    }
    {
    //Desigualdad !=: permite comparar si dos valores son diferentes.
    let a = 5;
    let b = 3;
    let resultado = a != b; // resultado = true
    }
    {
    //Mayor que >: permite comparar si un valor es mayor que otro.
    let a = 5;
    let b = 3;
    let resultado = a > b; // resultado = true
    }
    {
    //Menor que <: permite comparar si un valor es menor que otro.
    let a = 5;
    let b = 3;
    let resultado = a < b; // resultado = false
    }
    {
    //Mayor o igual que >=: permite comparar si un valor es mayor o igual que otro.
    let a = 5;
    let b = 5;
    let resultado = a >= b; // resultado = true
    }
    {
    //Menor o igual que <=: permite comparar si un valor es menor o igual que otro.
    let a = 5;
    let b = 3;
    let resultado = a <= b; // resultado = false
    }
    /*
    --2.2--Operadores de logicos:
    */
   {
   //AND &&: permite evaluar si dos condiciones se cumplen al mismo tiempo.
   let a = 5;
   let b = 3;
   let resultado = (a > 4) && (b < 4); // resultado = false
   }
   {
   //OR ||: permite evaluar si al menos una de dos condiciones se cumple.
   let a = 5;
   let b = 3;
   let resultado = (a > 4) || (b > 4); // resultado = true
   }   
   {
    //NOT !: permite negar una condición.
    let a = 5;
    let resultado = !(a > 4); // resultado = false
   }

   /*
   Estos son algunos de los operadores más comunes en JavaScript. 
   Es importante tener en cuenta que JavaScript tiene muchos otros 
   operadores que se utilizan en diferentes situaciones, y que la 
   comprensión de estos operadores es fundamental para poder escribir 
   código efectivo y eficiente.
   */