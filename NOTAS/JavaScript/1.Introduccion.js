/*
La lógica de programación en JavaScript incluye varios conceptos y
estructuras de control que permiten al programador controlar el
flujo de ejecución del programa. A continuación, te presento 
algunos de los conceptos más importantes con ejemplos.
*/ 
/*
--1--Variables: 
las variables son elementos que permiten 
almacenar datos en la memoria y utilizarlos en el programa. 
En JavaScript, se pueden definir variables utilizando las palabras 
reservadas var, let o const.
*/
//EJEMPLO
let nombre = 'Juan';
var edad = 25;
const PI = 3.1416;

/*
--2--Imprimir en consola:
Para imprimir un mensaje o un valor en la consola del navegador 
o de la consola del sistema operativo, se utiliza el método console.log().
*/
let miVariable = "Hola mundo";
console.log(miVariable); // muestra "Hola mundo" en la consola
/*
--3--Mostrar un aviso:
Para mostrar un aviso en una ventana emergente en el navegador, 
se utiliza el método alert().
*/
let variable = "Hola mundo";
alert(variable); // muestra "Hola mundo" en una ventana emergente
/*
--4--Operadores de concatenación:
El operador de concatenación (+) se utiliza para concatenar (unir) cadenas de texto.
*/
let saludo = "Hola";
let Nombre = "Juan";
console.log(saludo + " " + Nombre); // muestra "Hola Juan"
